package day1;

public class Exercise5 {

	public static void main(String[] args) {
		// add, subtract, multiply, divide and get remainder of 125,24
		int num1 = 125;
		int num2 = 24;

		System.out.println("125 + 24 = " + (num1 + num2));
		System.out.println("125 - 24 = " + (num1 - num2));
		System.out.println("125 x 24 = " + (num1 * num2));
		System.out.println("125 / 24 = " + (num1 / num2));
		System.out.println("125 % 24 = " + (num1 % num2));

	}

}
